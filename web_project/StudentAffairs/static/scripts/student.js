class Student{

    constructor(Id, Name, DateOfBirth, GPA, Level, Department, Email, Phone, Gender, Status){
        this.id = Id;
        this.name = Name;
        this.dateOfBirth = DateOfBirth;
        this.gpa = GPA;
        this.level = Level;
        this.department = Department;
        this.email = Email;
        this.phone = Phone;
        this.gender = Gender;
        this.status = Status;
    }

};